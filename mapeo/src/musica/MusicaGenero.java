
package musica;

/**
 *
 * @author chato
 */
public class MusicaGenero {
    private String Rock;
    private String Cumbias;
    private String Electronica;
    
    
     public MusicaGenero(String Rock, String Cumbias, String Electronica) {
        this.Rock = this.Rock;
        this.Cumbias = this.Cumbias;
        this.Electronica = this.Electronica;
    }
     
    public String getRock() {
        return Rock;
    }

    public String getCumbias() {
        return Cumbias;
    }

    public String getElectronica() {
        return Electronica;
    }


   




}
